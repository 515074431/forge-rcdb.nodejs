import ClientAPI from 'ClientAPI'

export default class MetaAPI extends ClientAPI {

  ///////////////////////////////////////////////////////////////
  // Class constructor
  //
  ///////////////////////////////////////////////////////////////
  constructor (apiUrl) {

    super (apiUrl)
  }

  ///////////////////////////////////////////////////////////////
  //
  //
  ///////////////////////////////////////////////////////////////
  getModelMetaProperties () {

    const url = this.apiUrl

    return this.ajax(url)
  }

  ///////////////////////////////////////////////////////////////
  //
  //
  ///////////////////////////////////////////////////////////////
  getNodeMetaProperties (nodeId) {

    const url = this.apiUrl + `/${nodeId}`

    return this.ajax(url)
  }

  ///////////////////////////////////////////////////////////////
  //
  //
  ///////////////////////////////////////////////////////////////
  addNodeMetaProperty (metaProperty) {

    const payload = {
      metaProperty
    }

    const url = this.apiUrl

    return this.ajax({
      url: url,
      method: 'PUT',
      headers: {
        'Accept': 'application/json',
        'Content-Type': 'application/json'
      },
      data: JSON.stringify(payload)
    })
  }

  ///////////////////////////////////////////////////////////////
  //
  //
  ///////////////////////////////////////////////////////////////
  deleteNodeMetaProperty (metaId) {

    const url = this.apiUrl + `/${metaId}`

    return this.ajax({
      url: url,
      method: 'DELETE'
    })
  }
}
